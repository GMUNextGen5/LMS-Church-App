/**
 * One-shot helper: slice index.html regions into login-shell.css and template fragments.
 * Run from repo root: node scripts/extract-index-slices.mjs
 */
import fs from 'node:fs';
import path from 'node:path';

const root = path.resolve(import.meta.dirname, '..');
const indexPath = path.join(root, 'index.html');
const indexRaw = fs.readFileSync(indexPath, 'utf8');
const lines = indexRaw.split(/\r?\n/);

const slice = (startLine, endLine) => lines.slice(startLine - 1, endLine).join('\n');

const styleOpen = indexRaw.indexOf('<style>');
const styleClose = indexRaw.indexOf('</style>', styleOpen);
if (styleOpen === -1 || styleClose === -1) {
  console.error(
    'index.html has no <style> block — cannot extract login-shell.css.\n' +
      'Restore from git history: node scripts/restore-login-shell-css.mjs'
  );
  process.exit(1);
}
const css = indexRaw.slice(styleOpen + '<style>'.length, styleClose).replace(/^\s*\n/, '');
fs.writeFileSync(
  path.join(root, 'src/assets/styles/login-shell.css'),
  css.trimEnd() + '\n',
  'utf8'
);

const registrationStudentInner = slice(8641, 9153);
const registrationTeacherInner = slice(9161, 9444);

const aiModal = slice(10440, 10477);
const legalModal = slice(10480, 10545);
const editStudentModal = slice(10548, 10661);
const changeRoleModal = slice(10664, 10788);

const escapeTpl = (s) => s.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$\{/g, '\\${');

const out = `/**
 * Deferred shell markup (injected on init for smaller initial HTML download + parse).
 * Generated by scripts/extract-index-slices.mjs — do not edit by hand without re-running.
 */
export const TEMPLATE_REGISTRATION_STUDENT_INNER = \`${escapeTpl(registrationStudentInner)}\`;

export const TEMPLATE_REGISTRATION_TEACHER_INNER = \`${escapeTpl(registrationTeacherInner)}\`;

export const TEMPLATE_AI_MODAL = \`${escapeTpl(aiModal)}\`;

export const TEMPLATE_LEGAL_MODAL = \`${escapeTpl(legalModal)}\`;

export const TEMPLATE_EDIT_STUDENT_MODAL = \`${escapeTpl(editStudentModal)}\`;

export const TEMPLATE_CHANGE_ROLE_MODAL = \`${escapeTpl(changeRoleModal)}\`;
`;

fs.writeFileSync(path.join(root, 'src/ui/templates.part.ts'), out, 'utf8');
console.log('Wrote login-shell.css and src/ui/templates.part.ts');
